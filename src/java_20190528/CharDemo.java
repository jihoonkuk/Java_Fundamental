package java_20190528;

public class CharDemo {
	public static void main(String[] args){
		//문자표현법
		char a = 'q';
		
		//유니코드 표현법 '\u0000'    
		
		char b = '\uAD6D';
		char c = '\uC9C0';
		char d = '\uD6C8';
		
		
		
		
	//아스키코드 표현법
		
		char e = 97;
		
		
		
		System.out.println(a);
		
		System.out.print(b);
		System.out.print(c);
		System.out.println(d);
		
		System.out.println(e);
		
		System.out.println ("국지훈  만세");
		System.out.println ("국지훈\\t만세");

	
		
		
				
	}

}
